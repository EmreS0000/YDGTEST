package com.library.management.service;

import com.library.management.entity.Book;
import com.library.management.entity.BookCopy;
import com.library.management.entity.BookCopyStatus;
import com.library.management.exception.BusinessException;
import com.library.management.exception.ResourceNotFoundException;
import com.library.management.repository.BookCopyRepository;
import com.library.management.repository.BookRepository;
import com.library.management.service.impl.BookCopyServiceImpl;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

import java.util.Arrays;
import java.util.List;
import java.util.Optional;

import static org.assertj.core.api.Assertions.*;
import static org.mockito.ArgumentMatchers.any;
import static org.mockito.ArgumentMatchers.anyLong;
import static org.mockito.Mockito.*;

@ExtendWith(MockitoExtension.class)
@DisplayName("BookCopyService Unit Tests")
class BookCopyServiceTest {

    @Mock
    private BookCopyRepository bookCopyRepository;

    @Mock
    private BookRepository bookRepository;

    @InjectMocks
    private BookCopyServiceImpl bookCopyService;

    private Book testBook;
    private BookCopy testBookCopy;

    @BeforeEach
    void setUp() {
        testBook = new Book();
        testBook.setId(1L);
        testBook.setTitle("Test Book");
        testBook.setIsbn("978-1234567890");

        testBookCopy = new BookCopy();
        testBookCopy.setId(1L);
        testBookCopy.setBook(testBook);
        testBookCopy.setBarcode("BC001");
        testBookCopy.setStatus(BookCopyStatus.AVAILABLE);
    }

    @Test
    @DisplayName("Should add copy successfully with provided barcode")
    void testAddCopy_Success_WithBarcode() {
        // Given
        when(bookRepository.findById(1L)).thenReturn(Optional.of(testBook));
        when(bookCopyRepository.findByBarcode("BC001")).thenReturn(Optional.empty());
        when(bookCopyRepository.save(any(BookCopy.class))).thenReturn(testBookCopy);

        // When
        bookCopyService.addCopy(1L, "BC001");

        // Then
        verify(bookCopyRepository, times(1)).save(any(BookCopy.class));
    }

    @Test
    @DisplayName("Should add copy successfully with auto-generated barcode")
    void testAddCopy_Success_AutoGeneratedBarcode() {
        // Given
        when(bookRepository.findById(1L)).thenReturn(Optional.of(testBook));
        when(bookCopyRepository.findByBarcode(any())).thenReturn(Optional.empty());
        when(bookCopyRepository.save(any(BookCopy.class))).thenReturn(testBookCopy);

        // When
        bookCopyService.addCopy(1L, null);

        // Then
        verify(bookCopyRepository, times(1)).save(any(BookCopy.class));
    }

    @Test
    @DisplayName("Should add copy with empty barcode string")
    void testAddCopy_Success_EmptyBarcode() {
        // Given
        when(bookRepository.findById(1L)).thenReturn(Optional.of(testBook));
        when(bookCopyRepository.findByBarcode(any())).thenReturn(Optional.empty());
        when(bookCopyRepository.save(any(BookCopy.class))).thenReturn(testBookCopy);

        // When
        bookCopyService.addCopy(1L, "");

        // Then
        verify(bookCopyRepository, times(1)).save(any(BookCopy.class));
    }

    @Test
    @DisplayName("Should throw exception when book not found")
    void testAddCopy_BookNotFound() {
        // Given
        when(bookRepository.findById(anyLong())).thenReturn(Optional.empty());

        // When/Then
        assertThatThrownBy(() -> bookCopyService.addCopy(99L, "BC001"))
                .isInstanceOf(ResourceNotFoundException.class)
                .hasMessageContaining("Book not found");
    }

    @Test
    @DisplayName("Should throw exception when barcode already exists")
    void testAddCopy_DuplicateBarcode() {
        // Given
        when(bookRepository.findById(1L)).thenReturn(Optional.of(testBook));
        when(bookCopyRepository.findByBarcode("BC001")).thenReturn(Optional.of(testBookCopy));

        // When/Then
        assertThatThrownBy(() -> bookCopyService.addCopy(1L, "BC001"))
                .isInstanceOf(BusinessException.class)
                .hasMessageContaining("Barcode already exists");
    }

    @Test
    @DisplayName("Should remove copy successfully")
    void testRemoveCopy_Success() {
        // Given
        when(bookCopyRepository.findById(1L)).thenReturn(Optional.of(testBookCopy));
        doNothing().when(bookCopyRepository).delete(testBookCopy);

        // When
        bookCopyService.removeCopy(1L);

        // Then
        verify(bookCopyRepository, times(1)).delete(testBookCopy);
    }

    @Test
    @DisplayName("Should throw exception when copy not found for removal")
    void testRemoveCopy_NotFound() {
        // Given
        when(bookCopyRepository.findById(anyLong())).thenReturn(Optional.empty());

        // When/Then
        assertThatThrownBy(() -> bookCopyService.removeCopy(99L))
                .isInstanceOf(ResourceNotFoundException.class)
                .hasMessageContaining("Copy not found");
    }

    @Test
    @DisplayName("Should throw exception when removing loaned copy")
    void testRemoveCopy_LoanedCopy() {
        // Given
        testBookCopy.setStatus(BookCopyStatus.LOANED);
        when(bookCopyRepository.findById(1L)).thenReturn(Optional.of(testBookCopy));

        // When/Then
        assertThatThrownBy(() -> bookCopyService.removeCopy(1L))
                .isInstanceOf(BusinessException.class)
                .hasMessageContaining("Cannot delete loaned copy");
    }

    @Test
    @DisplayName("Should remove reserved copy successfully")
    void testRemoveCopy_ReservedCopy() {
        // Given
        testBookCopy.setStatus(BookCopyStatus.RESERVED);
        when(bookCopyRepository.findById(1L)).thenReturn(Optional.of(testBookCopy));
        doNothing().when(bookCopyRepository).delete(testBookCopy);

        // When
        bookCopyService.removeCopy(1L);

        // Then
        verify(bookCopyRepository, times(1)).delete(testBookCopy);
    }

    @Test
    @DisplayName("Should get copies by book ID successfully")
    void testGetCopiesByBookId_Success() {
        // Given
        BookCopy copy2 = new BookCopy();
        copy2.setId(2L);
        copy2.setBook(testBook);
        copy2.setBarcode("BC002");
        copy2.setStatus(BookCopyStatus.AVAILABLE);

        List<BookCopy> copies = Arrays.asList(testBookCopy, copy2);
        when(bookCopyRepository.findByBookId(1L)).thenReturn(copies);

        // When
        List<BookCopy> result = bookCopyService.getCopiesByBookId(1L);

        // Then
        assertThat(result).isNotNull();
        assertThat(result).hasSize(2);
        assertThat(result).contains(testBookCopy, copy2);
        verify(bookCopyRepository, times(1)).findByBookId(1L);
    }

    @Test
    @DisplayName("Should return empty list when no copies found")
    void testGetCopiesByBookId_EmptyList() {
        // Given
        when(bookCopyRepository.findByBookId(1L)).thenReturn(Arrays.asList());

        // When
        List<BookCopy> result = bookCopyService.getCopiesByBookId(1L);

        // Then
        assertThat(result).isNotNull();
        assertThat(result).isEmpty();
        verify(bookCopyRepository, times(1)).findByBookId(1L);
    }

    @Test
    @DisplayName("Should handle adding multiple copies with different statuses")
    void testGetCopiesByBookId_MultipleStatuses() {
        // Given
        BookCopy copy2 = new BookCopy();
        copy2.setId(2L);
        copy2.setBarcode("BC002");
        copy2.setStatus(BookCopyStatus.LOANED);

        BookCopy copy3 = new BookCopy();
        copy3.setId(3L);
        copy3.setBarcode("BC003");
        copy3.setStatus(BookCopyStatus.RESERVED);

        List<BookCopy> copies = Arrays.asList(testBookCopy, copy2, copy3);
        when(bookCopyRepository.findByBookId(1L)).thenReturn(copies);

        // When
        List<BookCopy> result = bookCopyService.getCopiesByBookId(1L);

        // Then
        assertThat(result).isNotNull();
        assertThat(result).hasSize(3);
        assertThat(result.stream().filter(c -> c.getStatus() == BookCopyStatus.AVAILABLE)).hasSize(1);
        assertThat(result.stream().filter(c -> c.getStatus() == BookCopyStatus.LOANED)).hasSize(1);
        assertThat(result.stream().filter(c -> c.getStatus() == BookCopyStatus.RESERVED)).hasSize(1);
    }
}
